#include "oled.h"

extern I2C_HandleTypeDef hi2c1;

/* --------------------------------------------------------------------------
 * FP1 – Fonctions bas-niveau I2C (SSD1306)
 * -------------------------------------------------------------------------- */
void OLED_WriteCommand(uint8_t cmd) {
    HAL_I2C_Mem_Write(&hi2c1, OLED_I2C_ADDR, 0x00, 1, &cmd, 1, 10);
}

void OLED_WriteData(uint8_t data) {
    HAL_I2C_Mem_Write(&hi2c1, OLED_I2C_ADDR, 0x40, 1, &data, 1, 10);
}

/* --------------------------------------------------------------------------
 * FP1 – Initialisation du contrôleur SSD1306
 * -------------------------------------------------------------------------- */
void OLED_Fill(uint8_t color) {
    for (uint8_t page = 0; page < 8; page++) {
        OLED_WriteCommand(0xB0 + page);
        OLED_WriteCommand(0x00);
        OLED_WriteCommand(0x10);
        for (uint8_t col = 0; col < 128; col++)
            OLED_WriteData(color);
    }
}

void OLED_Init(void) {
    HAL_Delay(100);
    OLED_WriteCommand(0xAE);
    OLED_WriteCommand(0x20);
    OLED_WriteCommand(0x00);
    OLED_WriteCommand(0xC8);
    OLED_WriteCommand(0xA1);
    OLED_WriteCommand(0x8D);
    OLED_WriteCommand(0x14);
    OLED_WriteCommand(0xAF);
    OLED_Fill(0x00);
}

